var jour = 5;
var Nomdujour;

switch (jour) {
    case 1:
        Nomdujour = "Lundi";
        break; 

    case 2:
        Nomdujour = "Mardi";
            break;

    case 3:
                Nomdujour = "Mercredi";
                break;

    case 4:
                    Nomdujour = "Jeudi";
                    break;     

    case 5:
                      Nomdujour = "Vendredi";
                      break;

    case 6:
                        Nomdujour = "Samedi";
                          break;

    case 7:
                          Nomdujour = "Dimanche";
                              break;
                            
    default:
        Nomdujour = "Inconnu";
}
console.log(Nomdujour);